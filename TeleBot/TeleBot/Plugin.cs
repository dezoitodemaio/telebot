    public class PluginManager
    {
        public IEnumerable<Type> Plugins { get; private set; }

        public void Load()
        {
            string pluginPaths = @"Plugins\MainPlugin\bin\Debug\netcoreapp3.1\MainPlugin.dll";

            Assembly pluginAssembly = LoadPlugin(pluginPaths);

            Plugins = GetPlugins(pluginAssembly);

